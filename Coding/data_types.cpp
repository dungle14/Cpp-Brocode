#include <iostream>
using namespace std;
int main(){

    // integer
    int x = 5;
    int y = 6;
    int sum = x + y;
    cout << x << '\n';
    cout << y << '\n';
    cout << sum << '\n';

    // double
    double pi = 3.14;
    cout << pi << '\n';

    // character
    char grade = 'A';
    cout << grade;

    // boolean
    bool good = true;
    cout << good << '\n';

    // string
    string name = "TLA";
    cout << name << '\n';
    return 0;
    
}